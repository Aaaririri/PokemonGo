package pokemonGo;

public class Time {
	private String nome;
	private String lider;
	private String mascoteDoTime;
	private String cor;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getLider() {
		return lider;
	}
	public void setLider(String lider) {
		this.lider = lider;
	}
	public String getMascoteDoTime() {
		return mascoteDoTime;
	}
	public void setMascoteDoTime(String mascoteDoTime) {
		this.mascoteDoTime = mascoteDoTime;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	
	

}
